%% Basic examples
% format long, short, compact... (the %% separates the code in sections )
x=1/7000
format long
%%
% the , makes another instruction for the command prompt, in this case "x" (so it shows us the variable with its new format) 
format long e, x
%%
format long g, x
%%
% compact:
format compact, x
%%
% rat: rational approximation
format rat, x
